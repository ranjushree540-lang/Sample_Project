public class SimpleAddition {
    public static void main(String[] args) {
        // Declare two variables for storing numbers
        int num1 = 10;
        int num2 = 20;

        // Add the two numbers
        int sum = num1 + num2;

        // Print the result to the console
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
